// Stores and increments the amount of pieces added to man (incorrect guesses
// made by the player).
// ----------------------------------------------------------------------------
#include "man.h"

// ----------------------------------------------------------------------------
// Man class
// ----------------------------------------------------------------------------

// ----------------------------------------------------------------------------
// Man()
//
// Constructor. Initialises pieces to 0.
// ----------------------------------------------------------------------------
Man::Man()
{
	pieces = 0;
}

// ----------------------------------------------------------------------------
// addPiece()
//
// Add a piece to man (increment value of pieces).
// ----------------------------------------------------------------------------
void Man::addPiece()
{
	++pieces;
}

// ----------------------------------------------------------------------------
// getMan()
//
// Get man.
// ----------------------------------------------------------------------------
int & Man::getPieces()
{
	return pieces;
}
